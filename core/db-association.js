const classModel = require('../module/class/class.model')
const userClassModel = require('../module/user-class/user-class.model')
const classMateriModel = require('../module/class-materi/class-materi.model')
const classQuizModel = require('../module/class-quiz/class-quiz.model')
const quizDetailModel = require('../module/class-quiz/quiz-detail.model')
const roleModel = require('../module/role/role.model')
const userModel = require('../module/user/user.model')
const userClassHistoryModel = require('../module/user-class-history/user-class-history.model')
const meetingModel = require('../module/meeting/meeting.model')
const usulanMeetingModel = require('../module/usulan-meeting/usulan-meeting.model')

userModel.belongsTo(roleModel, { foreignKey: 'role_id', as: 'role' })
userClassModel.belongsTo(classModel, { foreignKey: 'class_id', as: 'class' })
userClassModel.belongsTo(userModel, { foreignKey: 'user_id', as: 'user' })
classMateriModel.belongsTo(classModel, { foreignKey: 'class_id', as: 'class' })
// classMateriModel.hasOne(classQuizModel, { foreignKey: 'class_id', as: 'quiz' })
classQuizModel.belongsTo(classMateriModel, { foreignKey: 'class_materi_id', as: 'class_materi' })
quizDetailModel.belongsTo(classModel, { foreignKey: 'class_quiz_id', as: 'class_quiz' })
userClassHistoryModel.belongsTo(classModel, { foreignKey: 'class_id', as: 'class' })
userClassHistoryModel.belongsTo(userModel, { foreignKey: 'user_id', as: 'user' })
userClassHistoryModel.belongsTo(classMateriModel, { foreignKey: 'class_materi_id', as: 'class_materi' })
userClassHistoryModel.belongsTo(classQuizModel, { foreignKey: 'class_quiz_id', as: 'class_quiz' })
meetingModel.belongsTo(usulanMeetingModel, { foreignKey: 'usulan_meeting_id', as: 'usulan_meeting' })
usulanMeetingModel.belongsTo(classModel, { foreignKey: 'class_id', as: 'class' })
